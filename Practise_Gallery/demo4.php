			<h2>German X-Ray Telescope Takes First Images of Universe. Here They Are!</h2><p>The first images from a dark-energy-hunting telescope have been revealed, and they're spectacular.

On Oct. 22, 2019, the inaugural images from the German X-ray telescope eROSITA were presented to the public at the Max Planck Institute for Extraterrestrial Physics (MPE) in Bavaria, Germany. These "first light" images are a combination of pictures from all seven of the telescope's Wolter-1 mirror modules, which have been scanning the sky with custom CCD cameras.The German space agency (DLR)'s eROSITA telescope, which has been making observations since Oct. 13, has taken combined X-ray images of pieces of the night sky. Those include pictures of the Large Magellanic Cloud (LMC) and A3391/3395, an interacting pair of galaxy clusters located about 800 million light-years from Earth.

Related: X-Ray Universe: Amazing Photos by the Chandra X-Ray Observatory

"These first images from our telescope show the true beauty of the hidden universe," Peter Predehl, principal investigator of eROSITA, said in a statement. "To meet our science goals, we needed enough sensitivity to detect the most-distant clusters of galaxies in the universe over the whole sky and resolve them spatially. These first light images show that we can do exactly that, but we can go a lot further."Between the telescope's CCD cameras and seven mirror modules, the observatory is extremely sensitive. "The potential for new discoveries is immense. Now we can start reaping the fruits of more than 10 years of work," Predehl added. 

A testament to the sensitivity of the instrument, eROSITA was able to capture some interesting details in the LMC, including remnants of the supernova SN 1987A and a number of both foreground stars and distant active galactic nuclei. 

The highly sensitive eROSITA telescope will not only help scientists capture detailed images of cosmic objects like stars and diffuse gas, but it could also support the investigation of dark energy, scientists said."This is a dream come true. We now know that eROSITA can deliver on its promise and create a map of the whole X-ray sky with unprecedented depth and detail," Andrea Merloni, eROSITA project scientist, said in the same statement. 

