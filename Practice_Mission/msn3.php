    The Interplanetary NanoSpacecraft Pathfinder In Relevant Environment project will demonstrate the revolutionary capability of deep space CubeSats by placing two nanospacecraft in Earth-escape orbit. Prior to any inclusion on larger planetary missions, CubeSats must demonstrate that they can operate, communicate, and be navigated far from Earth–these are the primary objectives of INSPIRE. Spacecraft components, such as a JPL X-band radio, and a robust watchdog system will provide the basis for future high-capability, lower-cost-risk missions beyond Earth. These components will enable future supplemental science and educational opportunities at many destinations. The nominal INSPIRE mission will last for three months and will achieve an expected Earth-probe distance of 1.5×10^8 km (dependent upon escape velocity as neither spacecraft will have propulsion capability). The project will monitor onboard telemetry; operate, communicate, and navigate with both spacecraft; demonstrate cross-link communications; and demonstrate science utility with an onboard magnetometer and imager. Lessons learned from this pathfinder mission will help to inform future interplanetary NanoSpacecraft and larger missions that might use NanoSpacecraft components. In this paper, we describe the INSPIRE mission concept, including the ground and flight systems, as well as provide a project status update as we build for an upcoming launch opportunity. Details will be provided on both the concept for operations and expected data return. Finally, initial lessons learned will be provided to the community. This work has been developed at the Jet Propulsion Laboratory, California Institute of Technology, under contract to NASA. Government sponsorship acknowledged.

     The Mars Cube One (MarCO), the first interplanetary CubeSat, successfully demonstrated that a CubeSat can operate, communicate, and navigate far from Earth (JPL 2019). Actually, several planetary CubeSat missions have already been proposed and scheduled for launch in the near future, including: the Interplanetary NanoSpacecraft Pathfinder In Relevant Environment (INSPIRE) ( Klesh et al. 2013), the Deep Interior Scanning CubeSat (DISCUS) ( Bambach et al. 2018), and DustCube, which is part of the Asteroid Impact Mission (AIM) ( Perez et al. 2018). Moreover, thirteen CubeSats (CuSP, LunaH-Map, Lunar Flashlight, Lunar IceCube, NEAScout, LunIR, EQUULEUS, OMOTENASHI, ArgoMoon, Cislunar Explorer A, B, CU-E3, and Miles) will travel into deep space aboard the maiden flight of the Space Launch System as a secondary payload (Alkalai 2018). ...
