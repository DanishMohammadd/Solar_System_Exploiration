The International Space Station (ISS) project aims at improving science and technology to enhance life and industries on the ground. Fifteen countries are participating in the ISS project including the United States, Russia, European Countries, Canada, and Japan. Japan has completed its manned space facility "the Japanese Experiment Module Kibo," and trains astronauts who will operate the ISS and Kibo at JAXA.
The space industry is commonly divided into four sectors: civil, defense, intelligence and commercial. Although these sectors operate programs largely independent of one another, they share an industrial base, workforce and infrastructure.

Often, the defense and intelligence sectors are jointly referred to as “national security space.” The defense sector refers primarily to Department of Defense (DoD) activities in support of the military. The intelligence sector encompasses all space-based or space-enabled intelligence capabilities that support national interests.

Civil space includes all non-defense government space activities, primarily those of the National Aeronautics and Space Administration (NASA) and the National Oceanic and Atmospheric Administration (NOAA).

Commercial space is generally segmented into satellite manufacturing, support ground equipment manufacturing, and the launch industry. The commercial sector includes prime contractors, entrepreneurial space companies and emerging markets for space capabilities.
The U.S. government pursues space activities in a number of areas. The President creates space policy for all government agencies and initiatives, while Congress is responsible for approving funding for and routine oversight of the space activities of the federal government.

Various White House offices are involved in setting out and implementing space policy – including the Office of Science and Technology Policy (OSTP), the National Security Council (NSC) and the Office of Management and Budget (OMB). These federal agencies are charged with executing the space activities of the U.S. government.

Another executive body called the National Space Council plays a large role in the formulation and execution of U.S. space policy. This group of advisors and experts existed from the early years of NASA through the Apollo program under a different name, and was reestablished once in 1989 for four years, and again in 2017. It is chaired by the Vice President of the United States.
