  <p>
    Lunar Flashlight is an exciting new mission concept that was recently selected by NASA’s Advanced Exploration Systems (AES) by a team from the Jet Propulsion Laboratory, UCLA, and Marshall Space Flight Center. This innovative, low-cost concept will map the lunar south pole for volatiles and demonstrate several technological firsts, including being the first CubeSat to reach the Moon, the first mission to use an 80 m2 solar sail, and the first mission to use a solar sail as a reflector for science observations.

Locating ice deposits in the Moon’s permanently shadowed craters addresses one of NASA’s Strategic Knowledge Gaps (SKGs) to detect composition, quantity, distribution, form of water/H species and other volatiles associated with lunar cold traps. The scientific and economic importance of lunar volatiles extends far beyond the question “is there water on the Moon?” Volatile materials including water come from sources central to NASA’s strategic plans, including comets, asteroids, interplanetary dust particles, interstellar molecular clouds, solar wind, and lunar volcanic and radiogenic gases. The volatile inventory, distribution, and state (bound or free, evenly distributed or blocky, on the surface or at depth, etc.) are crucial for understanding how these molecules interact with the lunar surface, and for utilization potential.

The Lunar Flashlight mission spacecraft maneuvers to its lunar polar orbit and uses its 80 m2 solar sail as a mirror to reflect 50 kW of sunlight down into shaded polar regions, while the on-board spectrometer measures surface reflection and composition. The Lunar Flashlight 6U spacecraft has heritage elements from predecessor systems including JPL’s INSPIRE and JPL’s experience with imaging spectrometers, including the Moon Mineralogy Mapper (M3). The deployable solar sail/reflector is based on previous solar sail experiments, scaled up for this mission. The mission will demonstrate a path where 6U CubeSats could, at dramatically lower cost than previously thought possible, explore, locate and estimate size and composition of ice deposits on the Moon. It is a game-changing capability for expanded human exploration, planetary science, heliophysics, and other relevant instrument applications.

Polar volatile data collected by Lunar Flashlight could then ensure that targets for more expensive lander- and rover-borne measurements would include volatiles in sufficient quantity and near enough to the surface to be operationally useful.

Dr. Barbara Cohen from NASA Marshall Space Flight Center is the Science Lead for Lunar Flashlight. Dr. Cohen is a member of several SSERVI teams and will be presenting the current status of the Lunar Flashlight mission at the upcoming NASA Exploration Science Forum (ESF) in July.

For more information, read the discussion presented at the third International Workshop on LunarCubes in Palo Alto, CA on November 13, 2013 or the whitepaper presented at the 2013 meeting of the Lunar Exploration Analysis Group (LEAG).

Posted by: Soderman/SSERVI Staff
Source: B. Cohen/ R. Staehle / NASA
