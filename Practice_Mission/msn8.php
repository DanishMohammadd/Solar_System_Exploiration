Cutting-edge technological power is required for conducting highly reliable aerospace activities that can attain safe and reliable achievements. For that reason, verification through research and development (R&D) is important, thus JAXA has been engaging in R&D in fundamental technologies that can be commonly applied in the aerospace field.
Technologies necessary for space projects that are exposed to the severe space environment are unique, and many areas are still unknown, hence researchs on them are leading-edge.

The main task of the Institute is to conduct high quality research in the areas which are the focus of the world's science and technology. The most important fields of the Institute's expertise include theoretical and applied mechanics, theory of coupled mechanical and physical fields, theoretical and experimental mechanics of materials and structures, computational methods in mechanics, acoustoelectronics, and ultrasonic medical diagnostics. Extensive research is also conducted in several branches of fundamental science and technology, such as: physics and thermodynamics of continua; plasma physics; stochastic dynamics; fluid mechanics; laser beam interaction with metal surfaces, nanophotonics, applied mathematics, applied informatics, and bio-informatics. Mission To be the reference centre of scientific and technological excellence, radical innovation and implementation of technology achievements into Polish scientific and industrial environment. To start up and participate in activities related to multifunctional materials, polymers, smart materials and technologies, biomedical applications of ultrasound and microfluidic technologies, applied information science, and innovation technologies. To provide first-rate PhD education in contemporary technology, mechanics, acoustics, computing, and its advances related to biomedical applications, enhanced by internationalization, links with industry, and the encouragement of a discovery spirit. To promote, support, drive and implement research and technology transfer initiatives and activities and links with industry and commerce to contribute to the sustainable development of society.
